def _validate_passphrase(passphrase: str) -> str | None:
    """Return an error message if passphrase is too weak, else None."""
    if len(passphrase) < 8:
        return ("Password too weak. Please use at least 8 characters including "
                "uppercase, lowercase, number, and special character.")
    if not re.search(r"[A-Z]", passphrase):
        return ("Password too weak. Please use at least 8 characters including "
                "uppercase, lowercase, number, and special character.")
    if not re.search(r"[a-z]", passphrase):
        return ("Password too weak. Please use at least 8 characters including "
                "uppercase, lowercase, number, and special character.")
    if not re.search(r"[0-9]", passphrase):
        return ("Password too weak. Please use at least 8 characters including "
                "uppercase, lowercase, number, and special character.")
    if not re.search(r"[!@#$%^&*()_+\-=\[\]{};':""\\|,.<>/?`~]", passphrase):
        return ("Password too weak. Please use at least 8 characters including "
                "uppercase, lowercase, number, and special character.")
    return None


