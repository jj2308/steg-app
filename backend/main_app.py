def _friendly_error(exc: Exception, context: str = "processing") -> str:
    """Convert a technical exception into a user-friendly message.

    The full traceback is logged server-side; only a simple sentence is
    returned for the API response.
    """
    raw = str(exc)
    logger.error("[%s] %s: %s", context, type(exc).__name__, raw)
    traceback.print_exc()

    for pattern, ctx_filter, friendly in _ERROR_MAP:
        if pattern.lower() in raw.lower():
            if ctx_filter is None or ctx_filter == context:
                return friendly

    # Fallback generic messages per context
    if context == "embed":
        return ("Something went wrong while hiding the data. "
                "Please try again or use a different file.")
    if context == "extract":
        return ("Something went wrong during extraction. "
                "Please check your file and passphrase and try again.")
    return "An unexpected error occurred. Please try again."


def _validate_file_extension(filename: str, media_type: str) -> str | None:
    """Return an error message if the file extension is unsupported."""
    ext = os.path.splitext(filename)[1].lower()
    if media_type == "image":
        if ext not in _IMAGE_EXTS:
            return ("This file format is not supported. "
                    "Please upload a PNG or JPG image.")
    else:
        if ext not in _VIDEO_EXTS:
            return ("This file format is not supported. "
                    "Please upload a supported video file (MP4, MOV, AVI, or MKV).")
    return None


