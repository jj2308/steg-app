@app.route("/api/extract", methods=["POST"])
def api_extract():
    """Extract and decrypt hidden data from stego media."""
    try:
        media_type = request.form.get("media_type", "image")
        passphrase = request.form.get("passphrase", "")
        stego_file = request.files.get("stego_file")

        # --- Validation ---
        if not stego_file or not stego_file.filename:
            return jsonify({"error": "Please upload a stego file."}), 400

        if not passphrase:
            return jsonify({"error": "Please enter the passphrase used during embedding."}), 400

        # --- Validate file format ---
        ext = os.path.splitext(stego_file.filename)[1].lower()
        if media_type == "image" and ext not in _IMAGE_EXTS:
            return jsonify({"error": "This file format is not supported. "
                                     "Please upload a PNG or JPG image."}), 400
        if media_type == "video" and ext not in _VIDEO_EXTS:
            return jsonify({"error": "This file format is not supported. "
                                     "Please upload a supported video file (MP4, MOV, AVI, or MKV)."}), 400

        stego_path = save_upload(stego_file)

        # --- Extract ---
        if media_type == "image":
            encrypted = image_steg.extract(stego_path, passphrase)
        else:
            payload_str = video_steg.extract(stego_path, passphrase)
            encrypted = payload_str.encode("ascii")

        # --- Decrypt ---
        decrypted = decrypt(encrypted, passphrase)

        # Unpack payload: recover original filename if present
        original_name, file_data = unpack_payload(decrypted)

        if original_name is not None:
            # File payload — restore with original filename
            download_filename = original_name
            out = output_path(download_filename)
            with open(out, "wb") as f:
                f.write(file_data)
            return jsonify({
                "success": True,
                "is_text": False,
                "media_type": media_type,
                "download_filename": download_filename,
                "decrypted_size": len(file_data),
            })
        else:
            # Plain text payload (no file header)
            try:
                message = file_data.decode("utf-8")
                return jsonify({
                    "success": True,
                    "is_text": True,
                    "media_type": media_type,
                    "message": message,
                    "decrypted_size": len(file_data),
                })
            except (UnicodeDecodeError, ValueError):
                download_filename = "extracted_file.bin"
                out = output_path(download_filename)
                with open(out, "wb") as f:
                    f.write(file_data)
                return jsonify({
                    "success": True,
                    "is_text": False,
                    "media_type": media_type,
                    "download_filename": download_filename,
                    "decrypted_size": len(file_data),
                })

    except RequestEntityTooLarge:
        raise  # let Flask's 413 handler respond
    except ValueError as e:
        logger.error("[extract] ValueError: %s", e)
        return jsonify({"error": "The passphrase is incorrect or the file does not contain hidden data."}), 400
    except Exception as e:
        return jsonify({"error": _friendly_error(e, "extract")}), 500


