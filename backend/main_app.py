@app.route("/api/embed", methods=["POST"])
def api_embed():
    """Embed secret data into a cover medium. Returns JSON metadata + download URL."""
    try:
        media_type = request.form.get("media_type", "image")
        passphrase = request.form.get("passphrase", "")
        cover_file = request.files.get("cover_file")
        secret_file = request.files.get("secret_file")
        secret_text = request.form.get("secret_text", "")

        # --- Validation ---
        if not cover_file or not cover_file.filename:
            return jsonify({"error": "Please upload a cover file."}), 400

        if not passphrase:
            return jsonify({"error": "Please enter a passphrase for encryption."}), 400

        pass_err = _validate_passphrase(passphrase)
        if pass_err:
            return jsonify({"error": pass_err}), 400

        if (not secret_file or not secret_file.filename) and not secret_text.strip():
            return jsonify({"error": "Please provide secret text or upload a secret file."}), 400

        # --- Validate file format ---
        fmt_err = _validate_file_extension(cover_file.filename, media_type)
        if fmt_err:
            return jsonify({"error": fmt_err}), 400

        # --- Save cover ---
        cover_path = save_upload(cover_file)

        # --- Prepare secret payload ---
        if secret_file and secret_file.filename:
            file_bytes = secret_file.read()
            secret_name = secret_file.filename
            raw_secret = pack_payload(secret_name, file_bytes)
        else:
            raw_secret = secret_text.strip().encode("utf-8")
            secret_name = "text input"

        # --- Encrypt (compress + AES-256) ---
        encrypted = encrypt(raw_secret, passphrase, compress=True)
        original_size = len(raw_secret)
        encrypted_size = len(encrypted)

        # --- Capacity check + embed ---
        capacity_info = {}
        metrics_info = {}
        output_filename = ""

        if media_type == "image":
            cap = estimate_image_capacity(cover_path)
            capacity_info = cap

            if encrypted_size > cap["max_bytes"]:
                return jsonify({
                    "error": ("The selected image does not have enough space to "
                              "hide the data. Please use a larger image.")
                }), 400

            stego_img = image_steg.embed(cover_path, encrypted, passphrase)
            output_filename = "stego_output.png"
            out_path = output_path(output_filename)
            stego_img.save(out_path, "PNG")

            try:
                metrics_info = compute_metrics(cover_path, out_path)
            except Exception:
                metrics_info = {"note": "Metrics unavailable for this pair."}

        else:
            # Video steganography
            cap = estimate_video_capacity(cover_path)
            capacity_info = cap
            payload_str = encrypted.decode("ascii")  # base64 is ASCII-safe
            output_filename = "stego_output.mkv"
            out_path = output_path(output_filename)

            job_id = request.form.get("job_id", "")

            def _stage_cb(stage, **kwargs):
                if job_id:
                    _job_progress[job_id] = {"stage": stage, **kwargs}

            result = video_steg.embed(
                cover_path, payload_str, passphrase=passphrase,
                output_path=out_path, on_stage=_stage_cb,
            )
            out_path = result["output_path"]
            output_filename = os.path.basename(out_path)
            capacity_info["frame_numbers"] = result["frame_numbers"]
            capacity_info["num_parts"] = result["num_parts"]

        file_hash = compute_hash(out_path)
        file_size = get_file_size_str(out_path)

        return jsonify({
            "success": True,
            "media_type": media_type,
            "download_filename": output_filename,
            "file_hash": file_hash,
            "file_size": file_size,
            "capacity": capacity_info,
            "metrics": metrics_info,
            "secret_name": secret_name,
            "secret_size": original_size,
            "encrypted_size": encrypted_size,
        })

    except RequestEntityTooLarge:
        raise  # let Flask's 413 handler respond
    except Exception as e:
        return jsonify({"error": _friendly_error(e, "embed")}), 500
    finally:
        job_id = request.form.get("job_id", "")
        _job_progress.pop(job_id, None)


@app.route("/api/embed/progress/<job_id>")
def embed_progress(job_id):
    """Poll current processing stage for a video embed job."""
    info = _job_progress.get(job_id, {"stage": "queued"})
    return jsonify(info)


